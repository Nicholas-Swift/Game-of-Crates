void Tilemap::Update(sf::Time &deltaTime)
{
	for(int i = 0; i < m_platforms.size(); i++)
	{
		m_platforms[i].Update();
		m_platforms[i].Move(deltaTime);
	}

	for(int i = 0; i < m_crates.size(); i++)
	{
		m_crates[i].Update(deltaTime, m_levelMap, m_platforms);
		CrateCrateCollision();
	}

}

